package Input;

public class Input {
    public String courseName;
    public String courseDescription;
    public String courseTarget;
    public int coursePrice;
    public String courseAttention;
    public String courseRemark;

    public Input(String courseName, String courseDescription, String courseTarget, int coursePrice, String courseAttention, String courseRemark){
        this.courseName = courseName;
        this.courseDescription = courseDescription;
        this.courseTarget = courseTarget;
        this.coursePrice = coursePrice;
        this.courseAttention = courseAttention;
        this.courseRemark = courseRemark;
    }
}
